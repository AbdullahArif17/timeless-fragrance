import { defineType, defineField } from 'sanity';

export default defineType({
  name: 'product',
  title: 'Product',
  type: 'document',
  fields: [
    defineField({
      name: 'name',
      title: 'Name',
      type: 'string',
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      options: {
        source: 'name',
        maxLength: 96,
      },
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'price',
      title: 'Price',
      type: 'number',
      validation: (Rule) => Rule.required().positive(),
    }),
    defineField({
      name: 'hasDiscount',
      title: 'Has Discount?',
      type: 'boolean',
      initialValue: false,
    }),
    defineField({
      name: 'discountPercent',
      title: 'Discount Percent',
      type: 'number',
      initialValue: 30,
      hidden: ({ parent }) => !parent?.hasDiscount,
      validation: (Rule) =>
        Rule.min(0)
          .max(100)
          .warning('Discount should be between 0 and 100'),
    }),
    defineField({
      name: 'image',
      title: 'Image',
      type: 'image',
      options: {
        hotspot: true,
      },
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'description',
      title: 'Description',
      type: 'text',
      validation: (Rule) => Rule.max(500),
    }),
  ],
});
